# Пример калькулятора с багами (исправленная версия)
def add(a, b):
    """Сложение двух чисел."""
    return a + b

def subtract(a, b):  # Исправлено: правильное написание функции
    """Вычитание второго числа из первого."""
    return a - b     # Исправлено: была операция сложения вместо вычитания

def multiply(a, b):
    """Умножение двух чисел."""
    return a * b

def divide(a, b):
    """Деление первого числа на второе с проверкой деления на ноль."""
    if b == 0:
        return "Ошибка: деление на ноль запрещено."
    return a / b

def main():
    print("Простой калькулятор")
    try:
        x = int(input("Введите первое число: "))
        y = int(input("Введите второе число: "))
    except ValueError:
        print("Ошибка: пожалуйста, введите корректные целые числа.")
        return

    print("Сложение:", add(x, y))
    print("Вычитание:", subtract(x, y))   # Исправлено: вызывается правильная функция
    print("Умножение:", multiply(x, y))
    print("Деление:", divide(x, y))

if __name__ == "__main__":
    main()
